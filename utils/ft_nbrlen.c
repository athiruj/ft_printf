#include "../ft_printf.h"

size_t	ft_nbrlen(long nbr)
{
	size_t	len;

	len = 1;
	if (nbr < 0)
		len++;
	nbr /= 10;
	while (nbr)
	{
		len++;
		nbr /= 10;
	}
	return (len);
}
